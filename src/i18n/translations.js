// Get current language from localStorage
export function getLanguage() {
  if (typeof window === 'undefined') return 'en'
  return localStorage.getItem('memora-lang') || navigator.language?.slice(0, 2) || 'en'
}

// Set language
export function setLanguage(code) {
  localStorage.setItem('memora-lang', code)
}

// Translation function — returns English fallback if key missing
export function t(key) {
  const lang = getLanguage()
  return translations[lang]?.[key] || translations.en[key] || key
}

// Get greeting based on time + language
export function getGreeting(name) {
  const hr = new Date().getHours()
  const key = hr < 12 ? 'greeting_morning' : hr < 17 ? 'greeting_afternoon' : 'greeting_evening'
  const greeting = t(key)
  const firstName = (name || '').trim().split(' ')[0]
  return firstName ? `${greeting}, ${firstName}` : greeting
}

export default translations
